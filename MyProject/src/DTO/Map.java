package DTO;

public class Map {
	private String name;
	private String map;
	public Map(String name, String map) {
		this.name = name;
		this.map = map;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getMap() {
		return map;
	}
	public void setMap(String map) {
		this.map = map;
	}
	
	
	
}
